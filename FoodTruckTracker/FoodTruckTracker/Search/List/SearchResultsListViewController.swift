extension SearchResultsListViewController: UITableViewDataSource {
    func numberOfSections(in tableView: UITableView) -> Int {
        fetchedResultsController?.sections?.count ?? 1
    }
    
    func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        fetchedResultsController?.sections?[section].numberOfObjects ?? 0
    }
    
    func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        guard let cell = tableView.dequeueReusableCell(
            withIdentifier: "TruckCell",
            for: indexPath) as? TruckListTableViewCell else {
                fatalError("Unable to cast cell as \(TruckListTableViewCell.self)")
        }
        
        cell.truck = fetchedResultsController?.object(at: indexPath)
        return cell
    }
}

